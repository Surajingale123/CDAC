package ass4q4;

public class Program {

	public static void main(String[] args) {
		
		int num =4;
		
		//int to double
		double d1=num;
		System.out.println(d1);
		
		//int to float
		float f1=num;
		System.out.println(f1);
		
		
		//int to boolean
		boolean b1=num!=0;
		System.out.println(b1);
		
		//int to string
		
		String str= String.valueOf(num);
		System.out.println(str);

	}

}
