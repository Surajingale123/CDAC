
//1. Write a program to calculate the sum of the first 50 natural numbers. 


public class sumof50 { 
    public static void main(String[] args) { 
        int lastsum=0 ; 
        for(int i=0;i<=50;i++){
         
         lastsum= lastsum+i;
         
       
        }
        System.out.println(lastsum);
        
    } 
}
